package Homework;

public class Variables {
    public static void main(String[] args) {

        //Variables: byte
        byte indexNumberOfTooth = 7;
        byte discountPercentage = 20;

        System.out.println(indexNumberOfTooth);
        System.out.println(discountPercentage);

        //Variables: short
        short numberOfFreeParkingLots = 230;
        short birthYear = 1999;
        short numberOfFreeCinemaSeats = 124;

        System.out.println(numberOfFreeCinemaSeats);
        System.out.println(birthYear);
        System.out.println(numberOfFreeParkingLots);

        //Variables: int
        int numberOfCallorhinusUrsinusLinnaeusPopulation = 1100000;
        int numberOfPatients = 641;
        int amountOfLoan = 25000;

        System.out.println(numberOfCallorhinusUrsinusLinnaeusPopulation);
        System.out.println(numberOfPatients);
        System.out.println(amountOfLoan);

        //Variables: Long
        long populationOfTheEarth = 8250423615L;

        System.out.println(populationOfTheEarth);

        //Variables: Char
        char percent = 37;
        char productGrade = 65;

        System.out.println(percent);
        System.out.println(productGrade);


        //Variables: double
        double oxygenAtomicMass = 15.99903;
        double ageOfCarYears = 10.5;

        System.out.println(oxygenAtomicMass);
        System.out.println(ageOfCarYears);

        //Variables: float
        float weightInKilogram = 223.4f;
        float heightInMeters = 1.73f;

        System.out.println(weightInKilogram);
        System.out.println(heightInMeters);

        //Variable: Boolean
        boolean isAdmin = true;
        boolean isChild = false;

        System.out.println(isAdmin);
        System.out.println(isChild);



    }
}
